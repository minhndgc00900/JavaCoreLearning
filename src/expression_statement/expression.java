package expression_statement;

public class expression
{
    public static void main(String[] args)
    {
        //expression includes variable, values, datatype.
        // component in the control flow statement (ex: if, else)
        //is also an expression. Everything in the bracket is also an expression


        //challenge
        int score = 100; //expression
        if(score > 90) //expression (in the bracket)
        {
            System.out.println("you got the high score"); //expression (in the bracket)
            score = 0; //expression
        }
    }
}


